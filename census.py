class Census:

    def __init__(self, rank, county, per_cap_in, med_house_in, med_fam_in, population, num_households):
        self.rank = rank
        self.county = county
        self.per_capita_income = per_cap_in
        self.median_household_income = med_house_in
        self.median_family_income = med_fam_in
        self.population = population
        self.num_households = num_households
